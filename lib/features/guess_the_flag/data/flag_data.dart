  // Filter out Antarctica-related entries (AQ) from gameplay
  static List<FlagData> get _playableFlags {
    return _flags.where((f) => f.countryCode != 'AQ').toList(growable: false);
  }

  static List<FlagData> getRandomFlags(int count) {
    final shuffled = List<FlagData>.from(_playableFlags)..shuffle();
    return shuffled.take(count).toList();
  }

  static FlagData getRandomFlag() {
    final list = _playableFlags;
    return list[DateTime.now().millisecondsSinceEpoch % list.length];
  }

  static List<String> getRandomOptions(FlagData correctFlag, int optionCount) {
    final options = <String>[correctFlag.countryName];
    final otherFlags =
        _playableFlags
            .where((flag) => flag.countryName != correctFlag.countryName)
            .toList();
    otherFlags.shuffle();

    for (int i = 0; i < optionCount - 1 && i < otherFlags.length; i++) {
      options.add(otherFlags[i].countryName);
    }

    options.shuffle();
    return options;
  }
